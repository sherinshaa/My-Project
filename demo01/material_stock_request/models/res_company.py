from odoo import fields, models


class ResCompany(models.Model):
    _inherit = 'res.company'

    temporary_account_id = fields.Many2one('account.account',
                                           string="Temporary Account",
                                           help="Choose a temporary account "
                                                "for stock transfer issue")
    material_request_journal_id = fields.Many2one('account.journal',
                                                  string="Material Request "
                                                         "Journal")
